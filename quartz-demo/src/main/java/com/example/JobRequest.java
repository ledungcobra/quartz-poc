package com.example;

public class JobRequest {
    private String name;
    private String group;
    private int interval;
    private String type;

    public String getName() {
        return name;
    }

    public String getGroup() {
        return group;
    }

    public int getInterval() {
        return interval;
    }

    public String getType() {
        return type;
    }
}
